# prod takes in the list, a starting position for elements you
# which to choose, the final element starting from there, and
# then computes the product.


def prod(arg1, arg2, arg3):
    m = 1
    for i in range(arg3):
        m = m*arg1[arg2 + i]
    return m


# x gives a list of each number in n.
x = [int(y) for y in list(n) if y != '\n']

# y collects the list of products of length ch sub-strings in n.
y = []

# z is a dictionary storing the string of numbers as well as the
# associated products.
z = {}

# ch is our choice of strings of numbers to select.
ch = 13

# you want i to go from 0 to 996
for i in range(len(x)-ch):
    y.append(prod(x, i, ch))
    z[prod(x, i, ch)] = [x[i] for i in range(i, i+ch, 1)]

# We calculate the largest product.
k = max(y)

for i in range(ch):
    print(f'{z[k][i]}*', end='', flush=True)

print(f" = {k}")
